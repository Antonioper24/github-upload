			<h2> ¿ Qu&eacute; es la Pr&oacute;tesis Dental ? </h2>
			<img class="foto" src="https://ceuskalduna.com/wp-content/uploads/2021/01/implantes-dentales.gif">
			<img class="foto2" src="https://static.wixstatic.com/media/1d01b6_2dac2762bd9a4bde9e843d60a9623735~mv2.gif">
			<br>
			<p>
				Es un elemento artifical destinado a restaura la anatom&iacute;a de una o varias piezas dentarias,
				restaurando tambi&eacute;n la relaci&oacute;n entre las maxilares, a la vez que devuelve la dimensi&oacute;n
				vertical y repone los dientes.
				<br>
				<br>
				Las pr&oacute;tesis dentales se fabrican de manera personalizada en materiales como el acr&icute;lico o la porcelana. 
				La base de la pr&oacute;tesis dental imita al tejido natural de la enc&iacute;a, con una cuidadosa pigmentaci&oacute;n 
				conocida como polimetilmetacrilato, que previene la acumulaci&oacute;n de bacterias.
				<br>
				<br>
				La funci&oacute;n de las pr&oacute;tesis dentales es rehabilitar la dentici&oacute;n en sus funciones fisiol&oacute;gicas, 
				como la masticaci&oacute;n y la desgluci&oacute;n de los alimentos, as&iacute; como evitar problemas de pronunciaci&oacute;n.
				La recuperaci&oacute;n de la est&eacute;tica dental es otro de los motivos por los que lleva a cabo este tratamiento.
			</p>	
		</div>
