			<h2> Los Tipos de Pr&oacute;tesis Que Ofrecemos </h2>
			<h4> Pr&oacute;tesis Removibles </h4>
			<img class="foto2" src="https://clinicatafur.com/wp-content/uploads/2017/09/protesis-parcial-removible.jpg">
			<img class="foto" src="https://dentalinfo.info/wp-content/uploads/2015/04/protesis3.gif">
			<p>
				Son las que pueden quitarse el propio paciente para higienizarlas. Seg&uacute;n las piezas que faltan pueden ser:
			</p>
			<br>
			<ol class="lista">
				<li> 
					<b>Parcial:</b> Cuando el paciente conserva algunos dientes y muelas, repone el resto con la pr&oacute;tesis. Seg&uacute;n 
					el material utilizado puede ser: 
					<ul> 
						<li> 
							<b>De resina y acr&iacute;lico:</b> Es el que es soportado por la enc&iacute;a y por los dientes adyacentes mediante
							ganchos met&aacute;licos que evitan que se mueva.
						</li>
						<li> 
							<b>Esquel&eacute;tico o Pr&oacute;tesis de estrctura met&aacute;lica con dientes de resina:</b> Es el tipo que va anclado por 
							medio de unos retenedores de diferente dise&ntilde;o. Transmite mayor presi&oacute;n a los dientes que los soportan.
						</li>
					</ul>
				</li>
				<br>
				<li> 
					<b>Completos:</b> Es utilizada cuando el paciente no conserva ning&uacute;n diente y la pr&oacute;tesis los repone todos. Abarca 
					toda la cresta &oacute;sea, y en el maxilar superior cubren todo el paladar duro para tratar de ganar retenci&oacute;n. Se 
					retienen las encias y su propias textura de la pr&oacute;tesis.
				</li>
			</ol>
		</div>
		<div></div>
		<div>	
			<h4> Pr&oacute;tesis Fijas </h4>
			<img class="foto2" src="http://www.nudentis.com/wp-content/uploads/2018/02/nudentis-protesis-fija-1.png">
			<img class="foto" src="https://userscontent2.emaze.com/images/ba6546e8-47c1-4f2c-bd22-6a84a476fb10/056a31fe8e503c12f985e5fa991a219f.gif">
			<p>
				Son aquellas que no pueden ser retiradas por el paciente, es decir, deber&aacute;n ser extra&iacute;das por un dentista en caso de ser 
				desalojadas. En ella se pueden encontrar las siguientes:
			</p>
			<br>
			<ol class="lista">
				<li> 
					<b>Corona o Puente fijos dentosoportados:</b> Estas pr&oacute;tesis sirven para restaurar uno o varias dientes. Se colocan sobre los 
					dientes previamente tallados, siendo ellos su &uacute;nico apoyo. Las coronas pueden ser de metal-cer&aacute;mica o de zirconio. 
				</li>
				<br>
				<li> 
					<b>Sobre Implantes:</b> Consta de una estructura prot&eacute;tica fijada mediante tornillos o cementados en los implantes que 
					se han colocado previamente dentro de los huesos maxilares.
				</li>
				<br>
				<li>
					<b>Pr&oacute;tesis H&iacute;bridas sobre Implantes:</b> Son de metal-acr&iacute;lico y suelen ser para desdentados totales. Son 
					dentaduras completas sobre estructuras met&aacute;licas, que se atornillan a los implantes. Se aconsejan a pacientes con una 
					reabsorci&oacute;n &oacute;sea considerable.
