			<h2>¿En qu&eacute; consiste una endodoncia?</h2>
			<img class="foto" src="https://i.pinimg.com/originals/1a/0b/d0/1a0bd045d136b744426a47a1cab56f2b.gif">
			<img class="foto2" src="https://media.v2.siweb.es/uploaded_thumb_big/662b70d6ef3d53022a8c15403a01c616/Endodoncia.jpg">
			<br>
			<p>
				La t&eacute;cnica consiste en extraer el nervio, limpiar el conducto de la ra&iacute;z y despu&eacute;s sellarlo con un material
				termopl&aacute;stico y cemento u otro tipo de materal de obturaci&oacute;n. Cuando ya se ha decido que el tratamiento adecuado ser&iacute;a una
				endodoncia, entonces se planificar&iacute;a la intervenci&oacute; de la siguiente manera:
				<br>
				<ol>
					<li> Como primer paso utilizaremos anestesia local.</li>
					<li> A continuaci&oacute;n se realiza un agujero en la corona del diente mediante el cual se accedera a la pulpa para su 
						extracci&oacute;n y se a&iacute;sla el diente de todo el organismo.
					</li>
					<li> Ahora se realizara un procedimiento de limpieza de los conductos para dejarlos as&eacute;pticos o limpios.</li>
					<li> A continuaci&oacute;n se realizara la fase de sellado del diente, a trav&eacute;s del cual se cierra los conductos que se
					han limpiado, dejando la pieza plenamente insensibilizada.</li>
					<li> Despu&eacute;s de la intervenci&oacute;n, se procedera a realizar una radiograf&iacute;a para verificar que todo 
					ha ido correctamente.</li>
				</ol> 
				<br>
				<br>
			</p>
		</div>
