			<h2>¿Qu&eacute; es la Endodoncia?</h2>
			<img class="foto" src="https://imaxilofacial.com/wp-content/uploads/2017/03/que-es-una-endodoncia-e1516657942881.png">
			<img class="foto2" src="https://www.cenytdental.com/wp-content/uploads/2020/01/endodoncia.jpg">
			<br>
			<p>
				La endodoncia es un tratamiento dental conocido com&uacute;nmente para &quot;matar el nervio&quot;. Consiste
				en eliminar la parte profunda del diente cuando se encuentra lesionado o infectado. El objetivo de este tratamiento
				es limpiar el diente por dentro y rellenarlo de un material inerte.<br>
				<br>
				<br>
				La endodoncias se realizan en piezas dentales con caries profundas, que propician la inflamaci&oacute;n o la necrosis 
				(muerte) de la pulpa dental. Otro motivos para la aparici&oacute;n de pulpitis o inflamaci&oacute;n de la pulpa son algunos
				tratamientos traumatismos, la abrasi&oacute;n, la erosi&oacute;n y el desgaste de los dientes por el roce entre ellos.
				<br>
				<br>
				La pulpa dental es la parte m&aacute;s interior del diente, donde se encuentra los nervios y los vasos sangu&iacute;neos. La 
				inflamaci&oacute;n de la pulpa suele manifestarse con dolor, que puede ser de distintos grados y darse en distintas ocasiones: 
				frente al calor o al fr&iacute;o, en determinadas posturas, al  comer o beber, etc. 
			</p>
		</div>
		<div></div>
