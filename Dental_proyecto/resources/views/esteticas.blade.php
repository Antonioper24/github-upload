			<h2>Algunos tratamientos</h2>
			<img class="foto2"src="https://static.wixstatic.com/media/68ccf3_6c4be70e884045afab516d71eb3a3b5f~mv2.gif">
			<img class="foto" src="https://cdn.shopify.com/s/files/1/1659/9429/files/ezgif-1-dbe8a5d2db_large_12581b95-fb68-433b-94da-1ae9ef16a7ae_large.gif">
			<p>
				La est&eacute;tica dental consiste en una serie de tratamientos, orientados a cada problema que presentan las bocas no arm&oacute;nicas. Entre ellas podemos encontrar las siguientes:
				<br>
				<br>
				<ol>
					<li>
						La Ortodoncia: Es un tratamiento que consiste en corregir la posición de dientes y arcadas dentarias para una restablecer el equilibrio en la forma y función de la boca y la cara, lo que también mejora la estética facial.
					</li>
					<li>
						Los Implantes: Es un tratamiento quir&uacute;rgico que consiste en sustituir dientes en mal estado o que se ha ca&iacute;do por pr&oacute;tesis. Las pr&oacute;tesis tienen el aspecto de dientes sanos, lo que tiene un claro beneficio est&eacute;tico.
					</li>
					<li>
						El Blanquiamiento: Es un procedimiento que se realiza con el objetivo de aclarar el color de los dientes cuando estos se han vuelto oscuros y amarillentos.
					</li>
					<li>
						La Carillas: Son unas finas l&aacute;minas de composite o porcelana que se colocan en la cara visible del diente para mejorar aspectos como su posici&oacute;, color, tama&ntilde;o o forma.
					</li>
					<li>
						La Gingivectom&iacute;a: Es una peque&ntilde;a ciru&iacute;a que se lleva a cabo bajo anestesia local. Consiste en reducir la cantidad de enc&iacute;a para que &eacute;ste tenga un tama&ntilde;o arm&oacute;nico y proporcionado.
					</li>
				</ol>
