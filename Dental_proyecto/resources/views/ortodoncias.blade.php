			<h2> Nuestros tratamientos </h2>
			<img class="foto2" src="https://www.ortodonciamg.com/images/aparatos/aparatos-metalicos.gif">
			<img class="foto" src="https://ortodonciasantcugat.com/wp-content/uploads/2019/04/ortodoncia-este%CC%81tica-ortodoncia-sant-cugat.png">
			<p>
				<b>Aparatos funcionales:</b> Son los aparatos dise&quot;ados para laterar la posici&oacute;n de los distintos 
				grupos de m&uacute;sculo que influyen sobre la funci&oacute;n y la posici&oacute;n de la mand&iacute;bula para 
				aumentar su longitud. Son aparatos que  un principio s&oacute;lo realizan modificaciones funcionales, pero que 
				luego dan lugar a cambios estructurales. Los efectos de estos aparatos son la inclinaci&oacute;n de los dientes y 
				los cambios en el hueso que soporta los dientes.<br>
				<br>
				 <b>Aparatos removibles:</b> Son los aparatos que el paciente se puede quitar para limpiarlos, pero cuando se los 
				 coloca van firmemente sujetos a los dientes. Estos aparatos aplican presiones controladas sobre los dientes que se 
				 desean desplazar mediante la acci&oacute;n de elementos mec&aacute;nicos activos, como resortes, arcos, tornillos, etc. 
				 Estas fuerzas son, en su totalidad, artificiales o mec&aacute;nico y act&uacute;an directamente desplazando los dientes.
				 <br>
				 <br>
				 <b>Aparatos Fijos:</b> Es una ortodoncia formada pro la combinaci&oacute;n de brackets, elementos met&aacute;lico o 
				 cer&aacute;mico pegados a los dientes y de arcos de metal que los atraviesan. Los aparatos fijos tienen capacidad de 
				 desplazar de forma individual todos y cada uno de los dientes en cualquier direcci&oacute;n, lo que no sucede con los 
				 dem&aacute;s aparatos.
			</p>
