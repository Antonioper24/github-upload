			<h2> ¿Cu&aacute;ndo se necesita un tratamiento de ortodoncia? </h2>
			<img class="foto2" src="https://ioa.es/wp-content/uploads/2019/03/ortodoncia-invisible-1-600x460.jpg">
			<img class="foto" src="https://i.pinimg.com/736x/77/34/59/77345936c97296f0f6ec2b4b73f0ec8f.jpg">
			<br>
			<p>
				Si tenemos algunos de las siguientes afecciones, es probable que necesitemos un tratamiento:
				<br>
				<br>
				<ol>
					<li><b> Sobremordida:</b> Los dientes superiores cubren completamente los dientes inferiores en oclusi&oacute;n.</li>
					<li><b> Mordida Invertida:</b> Los dientes inferiores sobresalen sobre los dientes superiores.</li>
					<li><b> Mordida Cruzada:</b> Uno o m&aacute;s dientes superiores ocluyen pro dentro de los dientes inferiores.</li>
					<li><b> Mordida Abierto:</b> Existe un espacio entre los dientes frontales superiores y los inferiores, que hace 
					que no cierren entre ellos, pero s&iacute; entre los dientes de atr&aacute;s.</li>
					<li><b> Desviaci&oacute;n de la l&iacute;nea media:</b> El centro de los dientes superiores no coinciden con el centro 
					de los dientes inferiores.</li>
					<li><b> Espaciamiento:</b> Separaciones o espacios entre los dientes como resultado de piezas faltantes o dientes 
					que no ocupan todo el espacio.</li>
					<li><b> Api&ntilde;amiento:</b> Cuando los dientes son demasiados grandes para ser acomodados en el espacio que ofrecen 
					los maxilares.</li>
				</ol>
			</p>
