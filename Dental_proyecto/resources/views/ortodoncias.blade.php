			<p>
				Es una especialidad odontol&oacute;gica que estudia, previene y corrige las alteraciones del desarrollo, las 
				formas de las arcadas dentarias y la posici&oacute;n de las maxilares, con el fin de establecer el equilibrio 
				morfol&oacute;gico y funcionales de la boca y de la cara, mejorando tambi&eacute;n la est&eacute;tica facial.
				<br>
				<br>
				El objetivo de la ortodoncia es la movilidad de los dientes buscando su normalizaci&oacute;n oclusal 
				(la superficie de masticaci&oacute;n), o el desplazamiento de tod el conjunto dentario, sobre todo, para corregir 
				trastornos funcionales de la masticaci&oacute;n.
				<br>
				<br>
				Los tratamientos ortod&oacute;ncicos tiene la ventaja de proporcionarnos una boca sana, una sonrisa de aspectos 
				agradables y dientes con mayores posibilidades de durar toda la vida.
