			<h2> Los Tipos de Implantes que Realizamos </h2>
			<img class="foto" src="https://static.miweb.padigital.es/var/m_f/f0/f03/61318/1053755-clinica_dental_en_gava_-_Implantologia__-_Animado_4.gif">
